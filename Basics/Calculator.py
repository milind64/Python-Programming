first = int(input("Enter first Operand : "))
operator = input("Enter (+,-,*,/,%)")
second = int(input("Enter second Operand : "))

if operator == '+':
    print(first+second)
if operator == '-':
    print(first-second)
if operator == '*':
    print(first*second)
if operator == '/':
    print(first/second)   
if operator == '%':
    print(first%second) 